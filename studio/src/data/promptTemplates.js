// 获取指定工具的所有分类
export function getCategories(toolType) {
  const templates = promptTemplates[toolType] || [];
  return [...new Set(templates.map(t => t.category))];
}

// 按分类过滤模版
export function filterTemplates(toolType, category) {
  const templates = promptTemplates[toolType] || [];
  if (!category || category === 'all') return templates;
  return templates.filter(t => t.category === category);
}
